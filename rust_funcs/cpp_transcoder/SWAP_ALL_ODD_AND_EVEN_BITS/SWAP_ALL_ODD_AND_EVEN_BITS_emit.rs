fn len(arr: &[i32]) -> usize {arr.len()}
#[no_mangle]
fn sort(arr: &mut [i32]) {arr.sort_by(cmpfunc);}

 
use std::f32;

#[no_mangle]
fn f_gold(x: u32) -> u32 {
    let mut even_bits = x & 0xAAAAAAAA; 
    let mut odd_bits = x & 0x55555555;
    even_bits >>= 1;
    odd_bits <<= 1;
    even_bits | odd_bits
}
